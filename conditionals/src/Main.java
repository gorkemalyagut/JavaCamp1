public class Main {
    public static void main(String[] args) {
        int num = 24;

        if(num < 20){
            System.out.println("The number is smaller than 20");
        } else if(num == 20){
            System.out.println("The number is equal to 20");
        } else {
            System.out.println("The number is greater than 20.");
        }

        if(num < 15){
            System.out.println("The number is smaller than 15");
        }
    }
}